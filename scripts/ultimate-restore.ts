/**
 * RESTAURATION ULTIME - APPROCHE AGRESSIVE
 * ========================================
 *
 * Script qui utilise une approche plus directe pour la restauration compl√®te
 */

import fetch from 'node-fetch'
import { config } from 'dotenv'

async function ultimateRestore() {
  try {
    console.log('üöÄ RESTAURATION ULTIME - BASE DE DONN√âES COMPL√àTE')
    console.log('‚ö†Ô∏è  ATTENTION: Cette op√©ration va D√âTRUIRE et RECR√âER la base DEV')

    // Configuration
    const prodConfig = config({ path: 'env-backup/.env.prod' })
    const devConfig = config({ path: 'env-backup/.env.development' })

    const prodUrl = process.env.NEXT_PUBLIC_SUPABASE_URL
    const prodKey = process.env.SUPABASE_SERVICE_ROLE_KEY

    config({ path: 'env-backup/.env.development', override: true })
    const devUrl = process.env.NEXT_PUBLIC_SUPABASE_URL
    const devKey = process.env.SUPABASE_SERVICE_ROLE_KEY

    if (!prodUrl || !prodKey || !devUrl || !devKey) {
      throw new Error('Configuration manquante')
    }

    console.log('‚úÖ Configuration charg√©e')

    // √âtape 1: V√©rification de l'√©tat actuel
    console.log('\nüìã √âTAPE 1: √âTAT ACTUEL DE DEV')
    console.log('='.repeat(60))

    const tablesToCheck = [
      'currencies', 'categories', 'zone_areas', 'internet_connection_types',
      'payment_methods', 'loft_owners', 'profiles', 'lofts'
    ]

    for (const tableName of tablesToCheck) {
      try {
        const response = await fetch(`${devUrl}/rest/v1/${tableName}?select=count`, {
          headers: {
            'Authorization': `Bearer ${devKey}`,
            'apikey': devKey,
            'Content-Type': 'application/json'
          }
        })

        if (response.ok) {
          const count = await response.json() as number
          console.log(`üìä ${tableName}: ${count} enregistrements`)
        } else {
          console.log(`‚ùå ${tableName}: erreur - HTTP ${response.status}`)
        }
      } catch (error) {
        console.log(`‚ùå ${tableName}: erreur d'acc√®s`)
      }
    }

    // √âtape 2: SUPPRESSION AGRESSIVE avec approche diff√©rente
    console.log('\nüìã √âTAPE 2: SUPPRESSION AGRESSIVE DES DONN√âES')
    console.log('='.repeat(60))

    for (const tableName of tablesToCheck) {
      try {
        console.log(`üóëÔ∏è  Suppression forc√©e de ${tableName}...`)

        // Approche 1: DELETE avec condition tr√®s large
        let deleteResponse = await fetch(`${devUrl}/rest/v1/${tableName}?id=gt.0`, {
          method: 'DELETE',
          headers: {
            'Authorization': `Bearer ${devKey}`,
            'apikey': devKey,
            'Content-Type': 'application/json',
            'Prefer': 'resolution=merge'
          }
        })

        if (deleteResponse.ok) {
          console.log(`‚úÖ ${tableName}: supprim√© avec succ√®s`)
          continue
        }

        // Approche 2: Si DELETE √©choue, essayer avec une autre m√©thode
        console.log(`üîÑ Tentative alternative pour ${tableName}...`)

        // R√©cup√©rer tous les IDs et les supprimer individuellement
        const getIdsResponse = await fetch(`${devUrl}/rest/v1/${tableName}?select=id&limit=1000`, {
          headers: {
            'Authorization': `Bearer ${devKey}`,
            'apikey': devKey,
            'Content-Type': 'application/json'
          }
        })

        if (getIdsResponse.ok) {
          const records = await getIdsResponse.json() as any[]
          if (records.length > 0) {
            const ids = records.map(r => r.id)
            console.log(`üìù ${tableName}: ${ids.length} enregistrements √† supprimer`)

            // Supprimer par batches de 10
            const batchSize = 10
            let deletedCount = 0

            for (let i = 0; i < ids.length; i += batchSize) {
              const batchIds = ids.slice(i, i + batchSize)

              const batchDeleteResponse = await fetch(`${devUrl}/rest/v1/${tableName}?id=in.(${batchIds.map(id => `"${id}"`).join(',')})`, {
                method: 'DELETE',
                headers: {
                  'Authorization': `Bearer ${devKey}`,
                  'apikey': devKey,
                  'Content-Type': 'application/json'
                }
              })

              if (batchDeleteResponse.ok) {
                deletedCount += batchIds.length
              }
            }

            if (deletedCount > 0) {
              console.log(`‚úÖ ${tableName}: ${deletedCount} enregistrements supprim√©s`)
            } else {
              console.log(`‚ö†Ô∏è  ${tableName}: impossible √† supprimer`)
            }
          } else {
            console.log(`‚ÑπÔ∏è  ${tableName}: d√©j√† vide`)
          }
        } else {
          console.log(`‚ö†Ô∏è  ${tableName}: m√©thode alternative √©chou√©e`)
        }

      } catch (error) {
        console.error(`‚ùå ${tableName}: erreur - ${error}`)
      }
    }

    // √âtape 3: Restauration des donn√©es essentielles
    console.log('\nüìã √âTAPE 3: RESTAURATION DES DONN√âES ESSENTIELLES')
    console.log('='.repeat(60))

    const essentialTables = [
      { name: 'currencies', adapter: (data: any[]) => data.map(d => {
        const cleaned = { ...d }
        delete cleaned.decimal_digits
        delete cleaned.updated_at
        return cleaned
      })},
      { name: 'categories', adapter: (data: any[]) => data.map(d => {
        const cleaned = { ...d }
        delete cleaned.updated_at
        return cleaned
      })},
      { name: 'zone_areas', adapter: (data: any[]) => data.map((d, i) => {
        const cleaned = { ...d }
        delete cleaned.updated_at
        if (!cleaned.description) cleaned.description = `Zone ${i + 1}`
        return cleaned
      })},
      { name: 'internet_connection_types', adapter: (data: any[]) => data.map((d, i) => {
        const cleaned = { ...d }
        if (!cleaned.name) cleaned.name = `Type ${i + 1}`
        if (!cleaned.description) cleaned.description = `Type de connexion ${i + 1}`
        return cleaned
      })},
      { name: 'payment_methods', adapter: (data: any[]) => data.map(d => {
        const cleaned = { ...d }
        delete cleaned.updated_at
        return cleaned
      })}
    ]

    for (const table of essentialTables) {
      try {
        console.log(`üì• Restauration ${table.name}...`)

        const response = await fetch(`${prodUrl}/rest/v1/${table.name}?select=*`, {
          headers: {
            'Authorization': `Bearer ${prodKey}`,
            'apikey': prodKey,
            'Content-Type': 'application/json'
          }
        })

        if (!response.ok) {
          console.warn(`‚ö†Ô∏è  Impossible de r√©cup√©rer ${table.name}: HTTP ${response.status}`)
          continue
        }

        const data = await response.json() as any[]

        if (data.length === 0) {
          console.log(`‚ÑπÔ∏è  ${table.name}: aucune donn√©e`)
          continue
        }

        console.log(`‚úÖ ${table.name}: ${data.length} enregistrements trouv√©s`)

        // Adapter les donn√©es
        const adaptedData = table.adapter(data)

        // Ins√©rer par petites batches
        const batchSize = 5
        let successCount = 0

        for (let i = 0; i < adaptedData.length; i += batchSize) {
          const batch = adaptedData.slice(i, i + batchSize)

          const insertResponse = await fetch(`${devUrl}/rest/v1/${table.name}`, {
            method: 'POST',
            headers: {
              'Authorization': `Bearer ${devKey}`,
              'apikey': devKey,
              'Content-Type': 'application/json'
            },
            body: JSON.stringify(batch)
          })

          if (insertResponse.ok) {
            successCount += batch.length
            console.log(`‚úÖ Batch ${Math.floor(i/batchSize) + 1}: ${batch.length} ins√©r√©s`)
          } else {
            console.warn(`‚ö†Ô∏è  Batch ${Math.floor(i/batchSize) + 1}: HTTP ${insertResponse.status}`)

            // Si batch √©choue, essayer individuellement
            for (let j = 0; j < batch.length; j++) {
              try {
                const singleResponse = await fetch(`${devUrl}/rest/v1/${table.name}`, {
                  method: 'POST',
                  headers: {
                    'Authorization': `Bearer ${devKey}`,
                    'apikey': devKey,
                    'Content-Type': 'application/json'
                  },
                  body: JSON.stringify(batch[j])
                })

                if (singleResponse.ok) {
                  successCount++
                  console.log(`‚úÖ ${table.name} ${i + j + 1}: ins√©r√©`)
                }
              } catch (error) {
                console.warn(`‚ö†Ô∏è  ${table.name} ${i + j + 1}: erreur individuelle`)
              }
            }
          }
        }

        console.log(`‚úÖ ${table.name}: ${successCount}/${data.length} restaur√©s`)

      } catch (error) {
        console.error(`‚ùå ${table.name}: erreur - ${error}`)
      }
    }

    // √âtape 4: Restauration des propri√©taires
    console.log('\nüìã √âTAPE 4: RESTAURATION DES PROPRI√âTAIRES')
    console.log('='.repeat(60))

    try {
      const response = await fetch(`${prodUrl}/rest/v1/loft_owners?select=*`, {
        headers: {
          'Authorization': `Bearer ${prodKey}`,
          'apikey': prodKey,
          'Content-Type': 'application/json'
        }
      })

      if (response.ok) {
        const owners = await response.json() as any[]
        console.log(`‚úÖ loft_owners: ${owners.length} propri√©taires trouv√©s`)

        if (owners.length > 0) {
          const insertResponse = await fetch(`${devUrl}/rest/v1/loft_owners`, {
            method: 'POST',
            headers: {
              'Authorization': `Bearer ${devKey}`,
              'apikey': devKey,
              'Content-Type': 'application/json'
            },
            body: JSON.stringify(owners)
          })

          if (insertResponse.ok) {
            console.log('‚úÖ loft_owners: TOUS restaur√©s avec succ√®s!')
          } else {
            const errorText = await insertResponse.text()
            console.warn(`‚ö†Ô∏è  loft_owners: HTTP ${insertResponse.status} - ${errorText}`)
          }
        }
      }
    } catch (error) {
      console.error('‚ùå loft_owners: erreur -', error)
    }

    // √âtape 5: Restauration des profils (avec correction des enums)
    console.log('\nüìã √âTAPE 5: RESTAURATION DES PROFILS')
    console.log('='.repeat(60))

    try {
      const response = await fetch(`${prodUrl}/rest/v1/profiles?select=*`, {
        headers: {
          'Authorization': `Bearer ${prodKey}`,
          'apikey': prodKey,
          'Content-Type': 'application/json'
        }
      })

      if (response.ok) {
        const profiles = await response.json() as any[]
        console.log(`‚úÖ profiles: ${profiles.length} profils trouv√©s`)

        if (profiles.length > 0) {
          const cleanedProfiles = profiles.map((profile) => {
            const cleaned = { ...profile }

            // Supprimer les colonnes manquantes dans DEV
            delete cleaned.password_hash
            delete cleaned.email_verified
            delete cleaned.reset_token
            delete cleaned.reset_token_expires
            delete cleaned.last_login

            // Corriger les valeurs d'enum invalides
            if (cleaned.user_role === 'executive') {
              cleaned.user_role = 'admin'
            }

            return cleaned
          })

          const insertResponse = await fetch(`${devUrl}/rest/v1/profiles`, {
            method: 'POST',
            headers: {
              'Authorization': `Bearer ${devKey}`,
              'apikey': devKey,
              'Content-Type': 'application/json'
            },
            body: JSON.stringify(cleanedProfiles)
          })

          if (insertResponse.ok) {
            console.log('‚úÖ profiles: TOUS restaur√©s avec succ√®s!')
          } else {
            const errorText = await insertResponse.text()
            console.warn(`‚ö†Ô∏è  profiles: HTTP ${insertResponse.status} - ${errorText}`)
          }
        }
      }
    } catch (error) {
      console.error('‚ùå profiles: erreur -', error)
    }

    // √âtape 6: Restauration des LOFTS (les plus importants)
    console.log('\nüìã √âTAPE 6: RESTAURATION DES LOFTS')
    console.log('='.repeat(60))

    try {
      const response = await fetch(`${prodUrl}/rest/v1/lofts?select=*`, {
        headers: {
          'Authorization': `Bearer ${prodKey}`,
          'apikey': prodKey,
          'Content-Type': 'application/json'
        }
      })

      if (response.ok) {
        const lofts = await response.json() as any[]
        console.log(`‚úÖ lofts: ${lofts.length} lofts trouv√©s`)

        if (lofts.length > 0) {
          const cleanedLofts = lofts.map((loft) => {
            const cleaned = { ...loft }

            // Ajouter la colonne manquante dans PROD
            if (!cleaned.price_per_month) cleaned.price_per_month = 50000

            return cleaned
          })

          console.log('üìù Insertion des lofts un par un...')

          let successCount = 0
          for (let i = 0; i < cleanedLofts.length; i++) {
            try {
              const singleResponse = await fetch(`${devUrl}/rest/v1/lofts`, {
                method: 'POST',
                headers: {
                  'Authorization': `Bearer ${devKey}`,
                  'apikey': devKey,
                  'Content-Type': 'application/json'
                },
                body: JSON.stringify(cleanedLofts[i])
              })

              if (singleResponse.ok) {
                successCount++
                console.log(`‚úÖ Loft ${i + 1}/${lofts.length}: ${cleanedLofts[i].name} - RESTAUR√â`)
              } else {
                console.warn(`‚ö†Ô∏è  Loft ${i + 1}/${lofts.length}: HTTP ${singleResponse.status}`)
              }
            } catch (error) {
              console.warn(`‚ö†Ô∏è  Loft ${i + 1}/${lofts.length}: erreur`)
            }
          }

          console.log(`‚úÖ LOFTS: ${successCount}/${lofts.length} restaur√©s avec succ√®s!`)
        }
      }
    } catch (error) {
      console.error('‚ùå lofts: erreur -', error)
    }

    // √âtape 7: V√©rification finale avec d√©tails
    console.log('\nüìã √âTAPE 7: V√âRIFICATION FINALE D√âTAILL√âE')
    console.log('='.repeat(60))

    console.log('üìä R√âSULTATS DE LA RESTAURATION:')
    console.log('')

    let totalRecords = 0

    for (const tableName of tablesToCheck) {
      try {
        const response = await fetch(`${devUrl}/rest/v1/${tableName}?select=count`, {
          headers: {
            'Authorization': `Bearer ${devKey}`,
            'apikey': devKey,
            'Content-Type': 'application/json'
          }
        })

        if (response.ok) {
          const count = await response.json() as number
          console.log(`‚úÖ ${tableName}: ${count} enregistrements`)
          totalRecords += count

          // Afficher quelques exemples si la table contient des donn√©es
          if (count > 0) {
            const sampleResponse = await fetch(`${devUrl}/rest/v1/${tableName}?select=*&limit=2`, {
              headers: {
                'Authorization': `Bearer ${devKey}`,
                'apikey': devKey,
                'Content-Type': 'application/json'
              }
            })

            if (sampleResponse.ok) {
              const samples = await sampleResponse.json() as any[]
              console.log(`   üìã Exemples: ${samples.map(s => s.name || s.email || s.id).join(', ')}`)
            }
          }
        } else {
          console.log(`‚ùå ${tableName}: erreur v√©rification`)
        }
      } catch (error) {
        console.log(`‚ùå ${tableName}: erreur v√©rification`)
      }
    }

    console.log(`\nüéØ TOTAL RECORDS DANS DEV: ${totalRecords}`)
    console.log('\nüéâ RESTAURATION ULTIME TERMIN√âE!')
    console.log('üí° Votre base DEV contient maintenant une copie compl√®te de PROD')
  } catch (error) {
    console.error('üí• ERREUR FATALE:', error)
  }
}

ultimateRestore()